namespace roundhouse\formbuilder\utilities;

use Craft;
use craft\base\Utility;

use roundhouse\formbuilder\assetbundles\formbuilderutilityutility\FormBuilderUtilityUtilityAsset;

class FormBuilderUtility extends Utility
{
    // Static
    // =========================================================================

    /**
     * Returns the display name of this utility.
     *
     * @return string The display name of this utility.
     */
    public static function displayName(): string
    {
        return Craft::t('form-builder', 'FormBuilderUtility');
    }

    /**
     * Returns the utility’s unique identifier.
     *
     * The ID should be in `kebab-case`, as it will be visible in the URL (`admin/utilities/the-handle`).
     *
     * @return string
     */
    public static function id(): string
    {
        return 'formbuilder-form-builder-utility';
    }

    /**
     * Returns the path to the utility's SVG icon.
     *
     * @return string|null The path to the utility SVG icon
     */
    public static function iconPath()
    {
        return Craft::getAlias("@roundhouse/formbuilder/assetbundles/formbuilderutilityutility/dist/img/FormBuilderUtility-icon.svg");
    }

    /**
     * Returns the number that should be shown in the utility’s nav item badge.
     *
     * If `0` is returned, no badge will be shown
     *
     * @return int
     */
    public static function badgeCount(): int
    {
        return 0;
    }

    /**
     * Returns the utility's content HTML.
     *
     * @return string
     */
    public static function contentHtml(): string
    {
        Craft::$app->getView()->registerAssetBundle(FormBuilderUtilityUtilityAsset::class);

        $someVar = 'Have a nice day!';
        return Craft::$app->getView()->renderTemplate(
            'form-builder/_components/utilities/FormBuilderUtility_content',
            [
                'someVar' => $someVar
            ]
        );
    }
}
